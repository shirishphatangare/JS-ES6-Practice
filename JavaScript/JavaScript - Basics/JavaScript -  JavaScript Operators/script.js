// Lecture - JavaScript Operators


// Addition
var a = 5 + 5;
console.log(a);

// Subtraction
var a = 5 - 5;
console.log(a);

// Multiplication
var a = 5 * 5;
console.log(a);

// Division
var a = 5 / 5;
console.log(a);

var a = (5 + 5) * 5;
console.log(a);


